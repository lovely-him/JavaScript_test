
let num1 = 100;

/* 加减乘除 */
console.log(num1 + 25);
console.log(num1 - 100)
console.log(num1 * 100); 
console.log(num1 / 1500); // 自动得到小数

let num2 = 100;

/* 更多运算 */
console.log(num2 % 1500); // Remainder
console.log(++num2); // Increment
console.log(--num2); // Decrement

